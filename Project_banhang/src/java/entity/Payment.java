package entity;

/**
 *
 * @author Hi
 */
public class Payment {
    private int id;
    private String method;

    public Payment(int id, String method) {
        this.id = id;
        this.method = method;
    }

    public Payment() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }
    
}
